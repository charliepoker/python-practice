# Demonstrating while loop to count from 0 to 9
i = 0
print('Running while loop')
while i < 10:
    print(i)
    i = i + 1
# Demonstrating for loop using range to count from 0 to 9
i = 0
print('Starting for loop')
for i in range(10):
    print(i)
# Demonstrating a for loop using a step
print('Showing a for loop example using range function and a step argument')
for i in range(0, 10, 2): # print numbers from 0 to 10 jumping by two steps
    print(i)
# Demonstrating a reverse number count loop
print('Using a reverse range to display numbers from top down')
for i in range(5, -1, -1): # print numbers from 5 down
    print(i)
 

